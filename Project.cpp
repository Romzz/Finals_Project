void Project::longest_collatz_sequence()    //This is like the reversed collatz method which serves as a solution
{
    Project proj;
    const int UPPER_LIMIT = 1000000;   // Don't calculate beyond this limit
   long long nextSeqNum;
   int chainCount;                  // Counts how many numbers are in a sequence chain
   int longChainStartNum;            // Holds the longest chain's starting number
   int longChainLength = 0;         // Holds the longest chain's length


   // Loop through all integers 1-1,000,000
   for (int n = 1; n <= UPPER_LIMIT; n++){
      chainCount = 1;
      nextSeqNum = n;

      // Test the number
      while (nextSeqNum > 1){
         // Even...
         if ((nextSeqNum % 2) == 0){
            nextSeqNum = nextSeqNum / 2;
         }
         // Odd...
         else {
            nextSeqNum = (3*nextSeqNum) + 1;
         }

         chainCount++;
      }

      // Compare and record the longest chain length
      if (chainCount > longChainLength){
         longChainLength = chainCount;
         longChainStartNum = n;
      }
   }

   cout << "The longest chain started at " << longChainStartNum << ".\n";
}

