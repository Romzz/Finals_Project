    while (!fi.eof ())
    {
        v.resize (v.size () + 1);
        count = 0;
        ch = fi.get ();
        while (ch != ',' && !fi.eof ())
        {
            v[v.size () - 1] += ch;
            ch = fi.get ();
            count++;
        }
    }

    sort (v.begin (), v.end ());

    for (unsigned int i = 0; i < v.size (); i++)
        total += (i + 1) * sum (v[i]);

    fi.close ();

    cout << total << endl
         << "Process took " << (static_cast<double> (clock()) - start) / CLOCKS_PER_SEC << " seconds." << endl;
